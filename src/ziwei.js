// ─── 메인 분석 함수 ───────────────────────────────────────
export function analyzeZiwei(birth) {
  try {
    const { year, month, day, hour, calendar } = birth;
    const h = hour ?? 12;

    const solar = calendar === 'lunar'
      ? Lunar.fromYmdHms(year, month, day, h, 0, 0).getSolar()
      : Solar.fromYmdHms(year, month, day, h, 0, 0);
    const lunar = solar.getLunar();

    const lunarMonth = Math.abs(lunar.getMonth()); // 윤달 = 음수 반환
    const lunarDay   = lunar.getDay();
    const yearGan    = lunar.getYearGan();

    const mingongIdx = calcMingongIdx(lunarMonth, hour);
    const bureau     = getBureau(yearGan, mingongIdx);
    const ziweiIdx   = calcZiweiIdx(lunarDay, bureau);
    const positions  = calcStarPositions(ziweiIdx);

    const mainStars = Object.entries(positions)
      .filter(([, idx]) => idx === mingongIdx)
      .map(([name]) => name);

    return {
      mingongIdx,
      mingongZhi:  ZHI[mingongIdx],
      mingongKo:   ZHI_KO[mingongIdx],
      bureau,
      bureauKo:    BUREAU_KO[bureau],
      lunarMonth,
      lunarDay,
      yearGan,
      ziweiIdx,
      ziweiZhi:    ZHI[ziweiIdx],
      mainStars,
      positions,
      profiles: mainStars.map(s => ({
        name:    s,
        nameKo:  STAR_NAMES_KO[s] || s,
        ...(STAR_PROFILE[s] || {}),
      })),
    };
  } catch (e) {
    return null;
  }
}
