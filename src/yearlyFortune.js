function pillarFromDate(year, month, day) {
  try {
    const ec = Solar.fromYmd(year, month, day).getLunar().getEightChar();
    return {
      yearGan: ec.getYearGan(), yearZhi: ec.getYearZhi(),
      monthGan: ec.getMonthGan(), dayGan: ec.getDayGan(), dayZhi: ec.getDayZhi(),
    };
  } catch { return null; }
}

function pad2(n) { return String(n).padStart(2, '0'); }

// 그 해의 '기후' 한 문장 — 천간·지지 오행으로 한 해 바탕 기운을 묘사
function yearClimate(gan, zhi, animal) {
  const ge = GAN_EL[gan], ze = ZHI_EL[zhi];
  if (!ge || !ze) return '';
  if (ge === ze) {
    return `<b>${animal}의 해</b>인 올해는 ${ge}(五行)의 기운이 두 겹으로 짙게 흐른다 — ${EL_CLIMATE[ge]}이 한 해 전체에 강하게 깔린다.`;
  }
  return `<b>${animal}의 해</b>인 올해는 천간 ${ge}·지지 ${ze}의 기운이 함께 흐른다 — ${EL_CLIMATE[ge]} 위로 ${EL_CLIMATE[ze]}이 더해지는 결이다.`;
}

function aspects(f) {
  return [
    { icon: '💰', label: '재물', text: f.money },
    { icon: '💼', label: '일·커리어', text: f.work },
    { icon: '❤️', label: '관계', text: f.relation },
    { icon: '🌿', label: '건강', text: f.health },
  ];
}

export function analyzeYearlyFortune(birth, userDayGan) {
  try {
    // 생일 일간은 음력/시주 경계까지 반영된 sajuDetail 값을 받아 쓰는 것이 정확하다.
    // (폴백: 양력 가정 계산 — 호출부가 dayGan을 안 넘긴 경우만)
    const dayGan = userDayGan || pillarFromDate(birth.year, birth.month, birth.day)?.dayGan;
    if (!dayGan) return null;

    const now = new Date();
    const cy = now.getFullYear(), cm = now.getMonth() + 1, cd = now.getDate();

    const todayP = pillarFromDate(cy, cm, cd);
    // 사주의 해 경계는 입춘(2월 초)이라, 년주(年柱)는 입춘 이후로 안전한 6/1 기준으로 계산
    const tyP = pillarFromDate(cy, 6, 1);
    const nyP = pillarFromDate(cy + 1, 6, 1);
    if (!todayP || !tyP) return null;

    const todayDayTG  = tenGod(dayGan, todayP.dayGan);
    const todayMonTG  = tenGod(dayGan, todayP.monthGan);
    const thisYearTG  = tenGod(dayGan, tyP.yearGan);
    const nextYearTG  = nyP ? tenGod(dayGan, nyP.yearGan) : null;

    const dayF  = TG_FORTUNE[todayDayTG]  || TG_FORTUNE['비견'];
    const tyF   = TG_FORTUNE[thisYearTG]  || TG_FORTUNE['비견'];
    const nyF   = nextYearTG ? (TG_FORTUNE[nextYearTG] || TG_FORTUNE['비견']) : null;

    const monMod = (TG_FORTUNE[todayMonTG]?.score || 60) >= 68 ? 4 : -3;
    const todayScore = Math.min(95, Math.max(30, dayF.score + monMod));

    const tyAnimal = ZHI_ANIMAL[tyP.yearZhi] || '';
    const nyAnimal = nyP ? (ZHI_ANIMAL[nyP.yearZhi] || '') : '';

    return {
      dayGan,
      today: {
        dateStr: `${cy}.${pad2(cm)}.${pad2(cd)}`,
        gan: todayP.dayGan, zhi: todayP.dayZhi,
        tenGod: todayDayTG, score: todayScore,
        label: dayF.label,
        flow: dayF.daily,
        aspects: null,
        advice: null,
      },
      thisYear: {
        year: cy, gan: tyP.yearGan, zhi: tyP.yearZhi, animal: tyAnimal,
        tenGod: thisYearTG, score: tyF.score, label: tyF.label,
        climate: yearClimate(tyP.yearGan, tyP.yearZhi, tyAnimal),
        flow: tyF.yearlyFlow,
        aspects: aspects(tyF),
        advice: tyF.yearlyAdvice,
      },
      nextYear: nyP ? {
        year: cy + 1, gan: nyP.yearGan, zhi: nyP.yearZhi, animal: nyAnimal,
        tenGod: nextYearTG, score: nyF?.score || 60, label: nyF?.label || nextYearTG,
        climate: yearClimate(nyP.yearGan, nyP.yearZhi, nyAnimal),
        flow: nyF?.yearlyFlow || '',
        aspects: nyF ? aspects(nyF) : null,
        advice: nyF?.yearlyAdvice || null,
      } : null,
    };
  } catch { return null; }
}
