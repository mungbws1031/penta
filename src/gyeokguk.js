export function analyzeGyeokguk(sajuDetail, counts) {
  const p = sajuDetail?.pillars;
  const dayGan = p?.dayGan;
  const monthZhi = p?.month?.zhi;
  if (!dayGan || !monthZhi) return null;
  const c = k => (counts && counts[k]) || 0;

  const hidden = ZHI_HIDDEN[monthZhi] || [];
  if (!hidden.length) return null;

  const jeonggiGod = tenGod(dayGan, hidden[0]); // 월지 정기 십신

  let key, basisStem = hidden[0], byTuchul = false;
  if (jeonggiGod === '비견') { key = '건록'; }
  else if (jeonggiGod === '겁재') { key = '양인'; }
  else {
    // 투출 검사 (년·월·시간, 정기 우선). 비겁은 정격을 이루지 않으므로 제외.
    const cheongan = [p.year?.gan, p.month?.gan, p.time?.gan].filter(Boolean);
    const revealed = hidden.filter(g => cheongan.includes(g) && !['비견', '겁재'].includes(tenGod(dayGan, g)));
    if (revealed.length) { basisStem = revealed[0]; byTuchul = true; }
    key = tenGod(dayGan, basisStem);
  }

  const g = GYEOK[key];
  if (!g) return null;

  // 성격(成格) / 파격(破格) 판정
  let warn = null, good = null;
  switch (key) {
    case '정관':
      if (c('상관')) warn = '사주에 상관(傷官)이 있어 관을 치니(傷官見官) 명예에 흠집·구설이 따르기 쉽다 — 이를 다스리는 인성(印星)이 절실하다.';
      else good = '관을 깨는 상관이 없어 격이 비교적 맑다 — 반듯하게 인정받는 길이 열려 있다.';
      break;
    case '상관':
      if (c('정관')) warn = '정관(正官)이 함께 있어 상관견관(傷官見官) — 재능과 규범이 정면충돌해 굴곡이 크다. 재성으로 흘리거나 인성으로 다스려야 한다.';
      else good = '관과 부딪히지 않아 재능을 마음껏 펼칠 수 있는 구조다.';
      break;
    case '식신':
      if (c('편인')) warn = '편인(偏印)이 식신을 빼앗으니(梟神奪食) 복록이 줄고 일이 막히기 쉽다 — 재성으로 편인을 제어해야 한다.';
      else good = '식신을 빼앗는 편인이 없어 복록이 온전한 구조다.';
      break;
    case '편인':
      if (c('식신')) warn = '식신을 보면 효신탈식(梟神奪食)으로 복이 새기 쉽다 — 재성으로 편인을 눌러주는 것이 관건이다.';
      break;
    case '정인':
      if (c('편재') + c('정재') >= 2) warn = '재성(財星)이 왕성해 인을 깨니(財剋印) 학업·명예·문서가 흔들리기 쉽다 — 재물 욕심을 절제해야 격이 산다.';
      else good = '인을 깨는 재성이 강하지 않아, 배움과 명예가 순조롭게 쌓이는 구조다.';
      break;
    case '정재':
    case '편재':
      if (c('비견') + c('겁재') >= 3) warn = '비겁(比劫)이 강해 재물을 다투니(群劫爭財) 들어와도 새기 쉽다 — 혼자 쥐기보다 협업·분배로 풀어야 한다.';
      else good = '재를 다투는 비겁이 과하지 않아, 번 것을 지킬 수 있는 구조다.';
      break;
    case '편관':
      if (!c('식신') && !c('상관') && !c('편인') && !c('정인'))
        warn = '칠살을 제압할 식상도, 살을 돌릴 인성도 약하다 — 제화(制化) 없는 칠살이라 압박에 시달리기 쉽다. 이 둘을 보강하는 것이 평생 과제.';
      else good = c('식신') || c('상관') ? '식상으로 칠살을 제압하는(食神制殺) 구조 — 시련을 실력으로 바꾸는 힘이 있다.'
        : '인성으로 살을 돌리는(殺印相生) 구조 — 압박을 권위와 지혜로 승화한다.';
      break;
    case '건록':
      if (c('편재') + c('정재') + c('편관') + c('정관') <= 1)
        warn = '힘은 강한데 재성·관성이 약해 흘려보낼 출구가 좁다 — 재·관을 키우는 것이 결실의 열쇠다.';
      else good = '강한 힘을 받아낼 재성·관성이 있어, 자수성가의 결실을 맺는 구조다.';
      break;
    case '양인':
      if (!c('편관') && !c('정관'))
        warn = '양인을 다스릴 관살(官殺)이 없어 그 힘이 사고·다툼으로 터지기 쉽다 — 절제와 규율이 반드시 필요하다.';
      else good = '관살이 양인을 제어하는(羊刃駕殺) 구조 — 강한 기운이 실력과 권위로 모인다.';
      break;
  }

  return {
    key, name: g.name, hanja: g.hanja, type: g.type, desc: g.desc, sangsin: g.sangsin,
    basisStem, byTuchul, jeonggiGod, warn, good,
  };
}
